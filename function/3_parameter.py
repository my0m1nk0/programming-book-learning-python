def calculator(num1 , num2,operator):
    if operator == "+":
        print("Total is =>",num1 + num2);
    elif operator == "-":
        print("Total is =>",num1 - num2);
    elif operator == "*":
        print("Total is =>",num1 * num2);
    elif operator == "/":
        print("Total is =>",num1 / num2);
    elif operator == "%":
        print("Total is =>",num1 % num2);
    else:
        print("Invalid operator");                      
    




